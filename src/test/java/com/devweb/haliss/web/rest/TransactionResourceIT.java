import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.*;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.*;

/**
 * Integration tests for the {@link TransactionResource} REST controller.
 */
@SpringBootTest(classes = HalissApp.class)
public class TransactionResourceIT {

    private static final LocalDate DEFAULT_DATE = LocalDate.ofEpochDay(0L);
    private static final LocalDate UPDATED_DATE = LocalDate.now(ZoneId.systemDefault());
    private static final LocalDate SMALLER_DATE = LocalDate.ofEpochDay(-1L);

    private static final String DEFAULT_FRAIS = "AAAAAAAAAA";
    private static final String UPDATED_FRAIS = "BBBBBBBBBB";

    private static final String DEFAULT_COM_ENVOI = "AAAAAAAAAA";
    private static final String UPDATED_COM_ENVOI = "BBBBBBBBBB";

    private static final String DEFAULT_COM_RETRAIT = "AAAAAAAAAA";
    private static final String UPDATED_COM_RETRAIT = "BBBBBBBBBB";

    private static final String DEFAULT_COM_SYSTEME = "AAAAAAAAAA";
    private static final String UPDATED_COM_SYSTEME = "BBBBBBBBBB";

    private static final String DEFAULT_COM_ETAT = "AAAAAAAAAA";
    private static final String UPDATED_COM_ETAT = "BBBBBBBBBB";

    @Autowired
    private TransactionRepository transactionRepository;

    @Autowired
    private TransactionService transactionService;

    @Autowired
    private MappingJackson2HttpMessageConverter jacksonMessageConverter;

    @Autowired
    private PageableHandlerMethodArgumentResolver pageableArgumentResolver;

    @Autowired
    private ExceptionTranslator exceptionTranslator;

    @Autowired
    private EntityManager em;

    @Autowired
    private Validator validator;

    private MockMvc restTransactionMockMvc;

    private Transaction transaction;

    @BeforeEach
    public void setup() {
        MockitoAnnotations.initMocks(this);
        final TransactionResource transactionResource = new TransactionResource(transactionService);
        this.restTransactionMockMvc = MockMvcBuilders.standaloneSetup(transactionResource)
            .setCustomArgumentResolvers(pageableArgumentResolver)
            .setControllerAdvice(exceptionTranslator)
            .setConversionService(createFormattingConversionService())
            .setMessageConverters(jacksonMessageConverter)
            .setValidator(validator).build();
    }

    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Transaction createEntity(EntityManager em) {
        Transaction transaction = new Transaction()
            .date(DEFAULT_DATE)
            .frais(DEFAULT_FRAIS)
            .comEnvoi(DEFAULT_COM_ENVOI)
            .comRetrait(DEFAULT_COM_RETRAIT)
            .comSysteme(DEFAULT_COM_SYSTEME)
            .comEtat(DEFAULT_COM_ETAT);
        return transaction;
    }
    /**
     * Create an updated entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Transaction createUpdatedEntity(EntityManager em) {
        Transaction transaction = new Transaction()
            .date(UPDATED_DATE)
            .frais(UPDATED_FRAIS)
            .comEnvoi(UPDATED_COM_ENVOI)
            .comRetrait(UPDATED_COM_RETRAIT)
            .comSysteme(UPDATED_COM_SYSTEME)
            .comEtat(UPDATED_COM_ETAT);
        return transaction;
    }

    @BeforeEach
    public void initTest() {
        transaction = createEntity(em);
    }

    @Test
    @Transactional
    public void createTransaction() throws Exception {
        int databaseSizeBeforeCreate = transactionRepository.findAll().size();

        // Create the Transaction
        restTransactionMockMvc.perform(post("/api/transactions")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(transaction)))
            .andExpect(status().isCreated());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeCreate + 1);
        Transaction testTransaction = transactionList.get(transactionList.size() - 1);
        assertThat(testTransaction.getDate()).isEqualTo(DEFAULT_DATE);
        assertThat(testTransaction.getFrais()).isEqualTo(DEFAULT_FRAIS);
        assertThat(testTransaction.getComEnvoi()).isEqualTo(DEFAULT_COM_ENVOI);
        assertThat(testTransaction.getComRetrait()).isEqualTo(DEFAULT_COM_RETRAIT);
        assertThat(testTransaction.getComSysteme()).isEqualTo(DEFAULT_COM_SYSTEME);
        assertThat(testTransaction.getComEtat()).isEqualTo(DEFAULT_COM_ETAT);
    }

    @Test
    @Transactional
    public void createTransactionWithExistingId() throws Exception {
        int databaseSizeBeforeCreate = transactionRepository.findAll().size();

        // Create the Transaction with an existing ID
        transaction.setId(1L);

        // An entity with an existing ID cannot be created, so this API call must fail
        restTransactionMockMvc.perform(post("/api/transactions")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(transaction)))
            .andExpect(status().isBadRequest());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeCreate);
    }


    @Test
    @Transactional
    public void getAllTransactions() throws Exception {
        // Initialize the database
        transactionRepository.saveAndFlush(transaction);

        // Get all the transactionList
        restTransactionMockMvc.perform(get("/api/transactions?sort=id,desc"))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_UTF8_VALUE))
            .andExpect(jsonPath("$.[*].id").value(hasItem(transaction.getId().intValue())))
            .andExpect(jsonPath("$.[*].date").value(hasItem(DEFAULT_DATE.toString())))
            .andExpect(jsonPath("$.[*].frais").value(hasItem(DEFAULT_FRAIS.toString())))
            .andExpect(jsonPath("$.[*].comEnvoi").value(hasItem(DEFAULT_COM_ENVOI.toString())))
            .andExpect(jsonPath("$.[*].comRetrait").value(hasItem(DEFAULT_COM_RETRAIT.toString())))
            .andExpect(jsonPath("$.[*].comSysteme").value(hasItem(DEFAULT_COM_SYSTEME.toString())))
            .andExpect(jsonPath("$.[*].comEtat").value(hasItem(DEFAULT_COM_ETAT.toString())));
    }
    
    @Test
    @Transactional
    public void getTransaction() throws Exception {
        // Initialize the database
        transactionRepository.saveAndFlush(transaction);

        // Get the transaction
        restTransactionMockMvc.perform(get("/api/transactions/{id}", transaction.getId()))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_UTF8_VALUE))
            .andExpect(jsonPath("$.id").value(transaction.getId().intValue()))
            .andExpect(jsonPath("$.date").value(DEFAULT_DATE.toString()))
            .andExpect(jsonPath("$.frais").value(DEFAULT_FRAIS.toString()))
            .andExpect(jsonPath("$.comEnvoi").value(DEFAULT_COM_ENVOI.toString()))
            .andExpect(jsonPath("$.comRetrait").value(DEFAULT_COM_RETRAIT.toString()))
            .andExpect(jsonPath("$.comSysteme").value(DEFAULT_COM_SYSTEME.toString()))
            .andExpect(jsonPath("$.comEtat").value(DEFAULT_COM_ETAT.toString()));
    }

    @Test
    @Transactional
    public void getNonExistingTransaction() throws Exception {
        // Get the transaction
        restTransactionMockMvc.perform(get("/api/transactions/{id}", Long.MAX_VALUE))
            .andExpect(status().isNotFound());
    }

    @Test
    @Transactional
    public void updateTransaction() throws Exception {
        // Initialize the database
        transactionService.save(transaction);

        int databaseSizeBeforeUpdate = transactionRepository.findAll().size();

        // Update the transaction
        Transaction updatedTransaction = transactionRepository.findById(transaction.getId()).get();
        // Disconnect from session so that the updates on updatedTransaction are not directly saved in db
        em.detach(updatedTransaction);
        updatedTransaction
            .date(UPDATED_DATE)
            .frais(UPDATED_FRAIS)
            .comEnvoi(UPDATED_COM_ENVOI)
            .comRetrait(UPDATED_COM_RETRAIT)
            .comSysteme(UPDATED_COM_SYSTEME)
            .comEtat(UPDATED_COM_ETAT);

        restTransactionMockMvc.perform(put("/api/transactions")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(updatedTransaction)))
            .andExpect(status().isOk());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeUpdate);
        Transaction testTransaction = transactionList.get(transactionList.size() - 1);
        assertThat(testTransaction.getDate()).isEqualTo(UPDATED_DATE);
        assertThat(testTransaction.getFrais()).isEqualTo(UPDATED_FRAIS);
        assertThat(testTransaction.getComEnvoi()).isEqualTo(UPDATED_COM_ENVOI);
        assertThat(testTransaction.getComRetrait()).isEqualTo(UPDATED_COM_RETRAIT);
        assertThat(testTransaction.getComSysteme()).isEqualTo(UPDATED_COM_SYSTEME);
        assertThat(testTransaction.getComEtat()).isEqualTo(UPDATED_COM_ETAT);
    }

    @Test
    @Transactional
    public void updateNonExistingTransaction() throws Exception {
        int databaseSizeBeforeUpdate = transactionRepository.findAll().size();

        // Create the Transaction

        // If the entity doesn't have an ID, it will throw BadRequestAlertException
        restTransactionMockMvc.perform(put("/api/transactions")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(transaction)))
            .andExpect(status().isBadRequest());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeUpdate);
    }

    @Test
    @Transactional
    public void deleteTransaction() throws Exception {
        // Initialize the database
        transactionService.save(transaction);

        int databaseSizeBeforeDelete = transactionRepository.findAll().size();

        // Delete the transaction
        restTransactionMockMvc.perform(delete("/api/transactions/{id}", transaction.getId())
            .accept(TestUtil.APPLICATION_JSON_UTF8))
            .andExpect(status().isNoContent());

        // Validate the database contains one less item
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeDelete - 1);
    }

    @Test
    @Transactional
    public void equalsVerifier() throws Exception {
        TestUtil.equalsVerifier(Transaction.class);
        Transaction transaction1 = new Transaction();
        transaction1.setId(1L);
        Transaction transaction2 = new Transaction();
        transaction2.setId(transaction1.getId());
        assertThat(transaction1).isEqualTo(transaction2);
        transaction2.setId(2L);
        assertThat(transaction1).isNotEqualTo(transaction2);
        transaction1.setId(null);
        assertThat(transaction1).isNotEqualTo(transaction2);
    }
}
